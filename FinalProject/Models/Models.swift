/// A single multiple-choice question generated by the AI.
struct QuizQuestion: Identifiable, Codable {
    var id: UUID = UUID()
    var question: String
    var options: [String]       // always 4 items (A / B / C / D)
    var correctIndex: Int       // 0 – 3
    var explanation: String
    var difficulty: Difficulty

    enum Difficulty: String, Codable, CaseIterable {
        case beginner, intermediate, advanced

        var label: String {
            switch self {
            case .beginner:     return "Beginner"
            case .intermediate: return "Intermediate"
            case .advanced:     return "Advanced"
            }
        }

        var color: Color {
            switch self {
            case .beginner:     return StudyTheme.shortBreakColor
            case .intermediate: return StudyTheme.warning
            case .advanced:     return StudyTheme.danger
            }
        }
    }
}

/// A completed or in-progress quiz attempt.
struct QuizSession: Identifiable, Codable {
    var id: UUID = UUID()
    var title: String
    var subject: String?
    var difficulty: QuizQuestion.Difficulty
    var questions: [QuizQuestion]
    /// Parallel array to `questions`. -1 means the question has not been answered yet.
    var userAnswers: [Int]
    var createdDate: Date = Date()
    var completedDate: Date?

    // MARK: Computed

    /// Number of correctly answered questions.
    var score: Int {
        zip(questions, userAnswers)
            .filter { question, answer in question.correctIndex == answer }
            .count
    }

    var totalQuestions: Int { questions.count }

    var percentage: Double {
        guard totalQuestions > 0 else { return 0 }
        return Double(score) / Double(totalQuestions)
    }

    var isCompleted: Bool { completedDate != nil }

    var isToday: Bool { Calendar.current.isDateInToday(createdDate) }

    var gradeLabel: String {
        switch percentage {
        case 0.9...:  return "Excellent"
        case 0.75...: return "Good"
        case 0.6...:  return "Fair"
        default:      return "Needs Review"
        }
    }

    var gradeColor: Color {
        switch percentage {
        case 0.9...:  return StudyTheme.success
        case 0.75...: return StudyTheme.accent
        case 0.6...:  return StudyTheme.warning
        default:      return StudyTheme.danger
        }
    }
}

