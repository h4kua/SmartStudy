/// A single flashcard with front (question/term) and back (answer/definition).
struct Flashcard: Identifiable, Codable {
    var id: UUID = UUID()
    var front: String
    var back: String
    var category: String
    var difficulty: QuizQuestion.Difficulty
    var reviewCount: Int  = 0
    var knewItCount: Int  = 0
    var lastReviewed: Date?

    var masteryPercent: Double {
        guard reviewCount > 0 else { return 0 }
        return Double(knewItCount) / Double(reviewCount)
    }

    var isMastered: Bool { reviewCount > 0 && masteryPercent >= 0.7 }
}

/// A named collection of flashcards on a single topic.
struct FlashcardDeck: Identifiable, Codable {
    var id: UUID = UUID()
    var title: String
    var subject: String?
    var cards: [Flashcard]
    var createdDate: Date      = Date()
    var lastReviewedDate: Date?

    // MARK: Computed

    var totalCards: Int { cards.count }

    var masteredCards: Int { cards.filter(\.isMastered).count }

    var overallMastery: Double {
        guard !cards.isEmpty else { return 0 }
        return Double(masteredCards) / Double(cards.count)
    }

    var isToday: Bool {
        guard let d = lastReviewedDate else { return false }
        return Calendar.current.isDateInToday(d)
    }

    var totalReviews: Int { cards.reduce(0) { $0 + $1.reviewCount } }
}

