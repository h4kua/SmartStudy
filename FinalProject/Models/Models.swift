/// Result of AI-powered analysis of a study document.
struct AnalyzedDocument: Identifiable, Codable {
    var id: UUID = UUID()
    var title: String
    var originalText: String
    var summary: String
    var keyConcepts: [String]
    var definitions: [String: String]   // term → definition
    var suggestedQuestions: [String]
    var subject: String?
    var analyzedDate: Date = Date()

    var isToday: Bool { Calendar.current.isDateInToday(analyzedDate) }

    /// Word count of the original text.
    var wordCount: Int { originalText.split(separator: " ").count }
}

// ============================================================
// MARK: - Color hex initialiser
// ============================================================

extension Color {
    init?(hex: String) {
        let h = hex.trimmingCharacters(in: .whitespacesAndNewlines)
                   .replacingOccurrences(of: "#", with: "")
        guard h.count == 6 else { return nil }
        var rgb: UInt64 = 0
        Scanner(string: h).scanHexInt64(&rgb)
        self.init(
            red:   Double((rgb >> 16) & 0xFF) / 255,
            green: Double((rgb >> 8)  & 0xFF) / 255,
            blue:  Double( rgb        & 0xFF) / 255
        )
    }
}

// ============================================================
// MARK: - Numeric helpers
// ============================================================

extension Int {
    /// Converts minutes to a human-readable string. e.g. 90 → "1h 30m"
    var minutesToHoursString: String {
        let h = self / 60
        let m = self % 60
        if h > 0 { return m > 0 ? "\(h)h \(m)m" : "\(h)h" }
        return "\(m)m"
    }
}

extension Double {
    /// 0.875 → "88%"
    var percentString: String { "\(Int((self * 100).rounded()))%" }
}
